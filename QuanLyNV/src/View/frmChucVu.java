    private boolean KTDL() throws HeadlessException {
        StringBuilder sb = new StringBuilder();
        if (txtTenChucVu.getText().equals("")) {
            sb.append("Vui lòng nhập tên chức vụ\n");
            txtTenChucVu.requestFocus();
        }
        if (txtHeSo.getText().equals("")) {
            sb.append("Vui lòng nhập hệ số\n");
            txtHeSo.requestFocus();
        }
        if (sb.length() > 0) {
            JOptionPane.showMessageDialog(this, sb.toString(), "Thông báo", JOptionPane.ERROR_MESSAGE);
            return false;
        } else {
            return true;
        }
    }

