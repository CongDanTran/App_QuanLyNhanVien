    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        if (them) {
            if (KTDL()) {
                try {
                    Connection conn = dao.getConnect();
                    String caulenh = "INSERT INTO tbl_chucvu VALUES(null,'" + txtTenChucVu.getText() + "','" + txtHeSo.getText() + "')";
                    boolean a = dao.Lenh(caulenh, conn);
                    if (a) {
                        tblChucVu.removeAll();
                        table.setRowCount(0);
                        loadNV();
                        JOptionPane.showMessageDialog(this, "Thêm thành công");
                    } else {
                        JOptionPane.showMessageDialog(this, "Thêm KHÔNG thành công");
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(frmChucVu.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
        if (sua) {
            Integer macv = Integer.parseInt(txtMaChucVu.getText());
            try {
                Connection conn = dao.getConnect();
                String caulenh = "UPDATE tbl_chucvu SET TenCV='" + txtTenChucVu.getText() + "',HeSo='" + txtHeSo.getText() + "'where MaCV=" + macv;
                boolean a = dao.Lenh(caulenh, conn);
                if (a) {
                    tblChucVu.removeAll();
                    table.setRowCount(0);
                    loadNV();
                    JOptionPane.showMessageDialog(this, "Sửa thành công");
                } else {
                    JOptionPane.showMessageDialog(this, "Sửa KHÔNG thành công");
                }
            } catch (SQLException ex) {
                Logger.getLogger(frmChucVu.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        if (xoa) {
            Integer macv = Integer.parseInt(txtMaChucVu.getText());
            try {
                Connection conn = dao.getConnect();
                String caulenh = "DELETE FROM tbl_chucvu WHERE MaCV=" + macv;
                boolean a = dao.Lenh(caulenh, conn);
                if (a) {
                    tblChucVu.removeAll();
                    table.setRowCount(0);
                    loadNV();
                    JOptionPane.showMessageDialog(this, "Xóa thành công");
                } else {
                    JOptionPane.showMessageDialog(this, "Xóa KHÔNG thành công");
                }
            } catch (SQLException ex) {
                Logger.getLogger(frmChucVu.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnLuuActionPerformed
